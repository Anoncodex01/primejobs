// Helper function to get all job titles as a flat array
export const getAllJobTitles = () => {
  const allTitles: string[] = [];
  Object.values(jobTitles).forEach(category => {
    allTitles.push(...category);
  });
  return allTitles.sort();
};

// Helper function to get job titles by category
export const getJobTitlesByCategory = (category: string) => {
  return jobTitles[category as keyof typeof jobTitles] || [];
};

// Helper function to get all categories
export const getJobCategories = () => {
  return Object.keys(jobTitles);
};
